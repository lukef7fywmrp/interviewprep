const toBinary = (hex) => {
  return hex.toString(2);
};

console.log(toBinary(0xff));
console.log(toBinary(0xaa));
console.log(toBinary(0xfa));
console.log(toBinary(0x00));
console.log(toBinary(0x01));
console.log(toBinary(0x0f));
console.log(toBinary(0x10));
console.log(toBinary(0x1f));
console.log(toBinary(0xa5));
console.log(toBinary(0xf0));
